package com.sergiorocasaez.videoentornos;

/**
 * Calculadora normalita
 * @author PC
 */
public class Calculadora {
    
    /**
     * Es el tipo de calculadora
     */
    public int tipoCalculadora;
    /**
     * Constructor sin parámetros
     */
    public Calculadora() { //dasñlkjasdflkñjasdf
    }

    /**
     * Suma dos números
     * @param a Un sumando
     * @param b Otro sumando
     * @return Devuelve la suma de los dos sumandos
     */
    int sumar(int a, int b) {
        return a + b;
    }

    /**
     * Resta dos números
     * @param a Minuendo
     * @param b Substraendo
     * @return Devuelve la resta de los dos parámetros
     */
    public int restar(int a, int b) {
        return a - b;
    }
}
